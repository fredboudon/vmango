def run(bloom_date, nb_fruits, nb_leaves, ms_init, k1_fruit_sample, verbose=False):
    bloom_date = datetime.datetime.strptime(bloom_date, '%d/%m/%Y').date()
    res = fruit_model_main(bloom_date, nb_fruits, nb_leaves, verbose=verbose, MS_Init=ms_init, k1_fruit_sample=k1_fruit_sample)
    res.to_csv('tmp/py.csv', na_rep='nan', index=False)

def run_v2(bloom_date, nb_fruits, nb_leaves, ms_init, k1_fruit_sample, input_hourly, input_daily, verbose=False):
    bloom_date = np.datetime64(datetime.datetime.strptime(bloom_date, '%d/%m/%Y')).astype('datetime64[D]')
    res = fruit_model_main(bloom_date, nb_fruits, nb_leaves, input_hourly, input_daily, verbose=verbose, MS_Init=ms_init, k1_fruit_sample=k1_fruit_sample)
    res.to_csv('tmp/py.csv', na_rep='nan', index=False)
